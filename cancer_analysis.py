def run_ttest(df, gcol, vcol, g1, g2, label):
    a = df[df[gcol] == g1][vcol].dropna()
    b = df[df[gcol] == g2][vcol].dropna()
    t, p = ttest_ind(a, b, equal_var=False)
    diff = a.mean() - b.mean()
    pooled_sd = np.sqrt((a.std()**2 + b.std()**2) / 2)
    d = diff / pooled_sd
    sig = 'SIGNIFICANT' if p < 0.05 else 'Not significant'
    print(f'\n{label}')
    print(f'  {g1}: n={len(a):,}  mean={a.mean():.2f}  SD={a.std():.2f}')
    print(f'  {g2}: n={len(b):,}  mean={b.mean():.2f}  SD={b.std():.2f}')
    print(f'  Delta={diff:.2f}  t={t:.3f}  p={p:.4e}  Cohen d={d:.3f}')
    print(f'  Result: {sig}')
    return dict(label=label, g1=g1, g2=g2,
                mean_g1=a.mean(), mean_g2=b.mean(),
                delta=diff, t=t, p=p, cohens_d=d)

