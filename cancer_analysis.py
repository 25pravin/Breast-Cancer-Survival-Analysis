def chi2_test(df, c1, c2, label):
    ct = pd.crosstab(df[c1], df[c2])
    chi2, p, dof, _ = chi2_contingency(ct)
    n = ct.values.sum()
    r, k = ct.shape
    cramers_v = np.sqrt(chi2 / (n * (min(r, k) - 1)))
    sig = 'SIGNIFICANT' if p < 0.05 else 'Not significant'
    print(f'\n{label}')
    print(f'  chi2={chi2:.3f}  df={dof}  p={p:.4e}  CramersV={cramers_v:.3f}')
    print(f'  Result: {sig}')
    return dict(label=label, chi2=chi2, p=p, dof=dof, cramers_v=cramers_v)

