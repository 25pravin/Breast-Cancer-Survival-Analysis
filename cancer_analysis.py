def run_anova(df, gcol, vcol, label):
    grps = df.groupby(gcol)[vcol].apply(list)
    f, p = f_oneway(*grps.values)
    grand_mean = df[vcol].mean()
    ss_b = sum(len(g) * (np.mean(g) - grand_mean)**2 for g in grps)
    ss_t = sum((x - grand_mean)**2 for g in grps for x in g)
    eta2 = ss_b / ss_t
    means = dict(df.groupby(gcol)[vcol].mean().round(2))
    sig = 'SIGNIFICANT' if p < 0.05 else 'Not significant'
    print(f'\n{label}')
    print(f'  Group means: {means}')
    print(f'  F={f:.3f}  p={p:.4e}  eta2={eta2:.4f}')
    print(f'  Result: {sig}')
    return dict(label=label, F=f, p=p, eta2=eta2)

